
# Define the radius of the circle
radius = 5  # You can change this value to any other value

# Calculate the circumference
circumference = 2 * 3.14159 * radius  # Using the formula 2 * π * r

# Calculate the area
area = 3.14159 * (radius ** 2)  # Using the formula π * r^2

# Print the variables
print("Circumference:", circumference)
print("Area:", area)